package com.AllanRibeiro;

public class Ceilling {
    private int hight;
    private int paintedColor;


    public Ceilling(int hight, int paintedColor) {
        this.hight = hight;
        this.paintedColor = paintedColor;
    }

    public int getHight() {
        return hight;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
}
